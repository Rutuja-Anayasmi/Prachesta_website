<script>
    document.addEventListener("DOMContentLoaded", function () {
        // Load header
        fetch('header.html')
            .then(response => response.text())
            .then(data => {
                document.getElementById('header-placeholder').innerHTML = data;
                highlightMenu();
                loadRazorpayButton(); // ‚úÖ Inject Razorpay after header is loaded
            });

        // Load footer
        fetch('footer.html')
            .then(response => response.text())
            .then(data => {
                document.getElementById('footer-placeholder').innerHTML = data;
            });
    });

    function highlightMenu() {
        if (!window.currentPage) return;

        var links = document.querySelectorAll('.nav.navbar-nav li a');
        links.forEach(function(link) {
            var page = link.getAttribute('data-page');
            if (page === window.currentPage) {
                link.classList.add('link-active');
            }
        });
    }

    function loadRazorpayButton() {
        const container = document.getElementById('razorpay-button-form');
        if (!container) return;

        container.innerHTML = ""; // clear old if any

        const script = document.createElement('script');
        script.src = "https://checkout.razorpay.com/v1/payment-button.js";
        script.setAttribute("data-payment_button_id", "pl_FR1fOi1ew0Qjjm");
        script.async = true;
        container.appendChild(script);
    }
</script>

